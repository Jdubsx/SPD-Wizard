    displayResults(matches, voltageInfo) {
        const resultContainer = document.getElementById('recommendationResult');
        
        if (matches.length === 0) {
            resultContainer.innerHTML = `
                <div class="no-results">
                    <i class="fas fa-exclamation-triangle"></i>
                    <h3>No matching SPDs found</h3>
                    <p>No surge protection devices match your specified criteria. Try adjusting your requirements or contact our support team for assistance.</p>
                </div>
            `;
            return;
        }

        // Sort matches by type and surge rating
        matches.sort((a, b) => {
            if (a.type !== b.type) {
                return a.type === 'Type 1' ? -1 : 1;
            }
            const aRating = parseInt(a.maxSurgeCurrentRating) || 0;
            const bRating = parseInt(b.maxSurgeCurrentRating) || 0;
            return bRating - aRating;
        });

        let html = `
            <div class="results-header">
                <h3><i class="fas fa-shield-alt"></i> Recommended SPDs</h3>
                <p>Found ${matches.length} surge protection device(s) matching your requirements:</p>
                <div class="system-info">
                    <strong>System:</strong> ${this.formData.systemVoltage}
                </div>
            </div>
            <div class="results-grid">
        `;

        matches.forEach((device, index) => {
            html += this.createDeviceCard(device, false);
        });

        html += '</div>';
        
        resultContainer.innerHTML = html;
    }

